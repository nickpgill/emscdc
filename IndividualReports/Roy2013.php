<ol>
<li>I am CIMPA scientific officer for subsaharian Africa jointly with Giulia Di Nunno. See <a href = "http://www.cimpa-icpam.org/">here</a> and more specifically <a href = "http://www.cimpa-icpam.org/spip.php?rubrique45&lang=en">here</a>.

CIMPA considers subsaharian Africa as a priority zone for its activities. A CIMPA Africa team meets by telephone about three times a year and discuss activities past and future.
 
Main CIMPA activities in subsaharian Africa are
<ul>
<li>3 to 4 research schools per year
<li>African Mathematical Schools organized jointly with AMU
<li>women in mathematics workshop, jointly with AMU (the first one in Ouagadougou in 2012, the second one in AIMS South Africa in 2013)
<li>support to various training activities in mathematical research
</ul>
<li>I am the CIMPA representative inside a club of french universities and research institutions acting for mathematics in subsaharian Africa, <a href = "http://www.latp.univ-mrs.fr/~sarima/">SARIMA</a>
<li>I am together with Jean Laurent in charge of the <a href = "http://www.cimpa-icpam.org/spip.php?article454">Ibni Prize</a>.
<li>I have been involved in the launching of the Simons Foundation Africa Mathematics project. Out of over 30 proposals, 3 are finally funded by the Simons Foundation. They will start in may 2013 for a first period of 5 years, that can be renewed. I have accepted to be the facilitator of two of these projects.
</ol></p>


